public class OperadoresComparacao {
    public static void main(String[] args) {
        int numeroUm, numeroDois;
        numeroUm = 10;
        numeroDois = 20;

        System.out.println("10 > 20: " + (numeroUm > numeroDois));
        System.out.println("10 >= 20: " + (numeroUm >= numeroDois));
        System.out.println( "10 < 20: " + (numeroUm < numeroDois));
        System.out.println( "10 <= 20: " + (numeroUm <= numeroDois));
        System.out.println( "10 == 20: " + (numeroUm == numeroDois));
        System.out.println( "10 != 20: " + (numeroUm != numeroDois));

    }
}
