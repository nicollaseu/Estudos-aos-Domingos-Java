public class OperadoresLogicos {
    public static void main(String[] args) {

        // && ||

        int idade = 15;
        double altura = 1.60;

        System.out.println(idade >= 18 || altura >= 1.60 );
        System.out.println(idade >= 18 && altura >= 1.60 );
        System.out.println(idade >= 18 && altura >= 1.60 );
    }
}
